function Vehicle(color, engine) {
    this.color = color;
    this.engine = engine;
    this.maxSpeed = 70;
}

function Car(model, color, engine) {
    this.color = color;
    this.engine = engine;
    this.model = model;
    this.maxSpeed = 80;
}

function Motorcycle(model, color, engine) {
    this.color = color;
    this.engine = engine;
    this.model = model;
    this.maxSpeed = 90;
}


